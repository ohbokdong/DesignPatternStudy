package deep_shallow_review0527;

public class Age {

	int year; // 태어난 해
	int value; // 나이 값
	
	public Age(int year, int value) {
		this.year = year;
		this.value = value;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
	
}
